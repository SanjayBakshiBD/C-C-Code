#include<stdio.h>
int main()
{
    int a;
    int n=1;
    int t=1;

    scanf("%d", &n);
    scanf("%d", &t);

    while(n<=a)
    {
        int s=1;
        while(s<=t)
        {
            printf("%d", s);
            s=s+1;
        }
        a++;
        printf("\n");

    }
    return 0;
}
